public class PatternProgr_1 {

	public static void main(String[] args) {
		//i for rows & j for coloum
		for(int i=1;i<=5;i++) {
			for(int j=1;j<=5;j++) {
				//System.out.print(" "+i);
				System.out.print(" "+"*");
			}
			System.out.println();//it is for come to next line 
		}

	}

}
